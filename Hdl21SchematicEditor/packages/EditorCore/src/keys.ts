// # Keyboard Inputs
// (That we care about)
export enum Keys {
  c = "c", // Circle (symbol) / Code Prelude
  i = "i", // Instance
  l = "l", // Line (symbol)
  p = "p", // Port
  t = "t", // Text (symbol)
  w = "w", // Wire
  r = "r", // Rotate / Rectangle (symbol)
  h = "h", // Horizontal flip
  v = "v", // Vertical flip
  z = "z", // Undo / redo
  s = "s", // Save
  Comma = ",", // Save(?)
  Escape = "Escape", // Cancel
  Backspace = "Backspace", // Remove
  Delete = "Delete", // Remove
  Enter = "Enter", // Finish
  Space = " ", // Filter this out of names
}
